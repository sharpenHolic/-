void Student::sShowAllApppiontment()
{
	OrderFile of;
	if (of.oSize == 0)
	{
		cout << "无预约记录" << endl;
		system("pause");
		system("cls");
		return;
	}

	for (int i = 0; i < of.oSize; i++)
	{
		cout << i + 1 << "、 ";

		cout << "预约日期： 周" << of.oOrderData[i]["日期"];
		cout << " 时段：" << (of.oOrderData[i]["时间段"] == "1" ? "上午" : "下午");
		cout << " 学号：" << of.oOrderData[i]["学号"];
		cout << " 姓名：" << of.oOrderData[i]["学生姓名"];
		cout << " 机房：" << of.oOrderData[i]["机房号"];
		string status = " 状态： ";  // 0 取消的预约   1 审核中   2 已预约 -1 预约失败
		if (of.oOrderData[i]["预约信息"] == "1")
		{
			status += "审核中";
		}
		else if (of.oOrderData[i]["预约信息"] == "2")
		{
			status += "预约成功";
		}
		else if (of.oOrderData[i]["预约信息"] == "-1")
		{
			status += "审核未通过，预约失败";
		}
		else
		{
			status += "预约已取消";
		}
		cout << status << endl;
	}

	system("pause");
	system("cls");
}


void Student::sCancelAppointment()
{
	OrderFile of;
	if (of.oSize == 0)
	{
		cout << "无预约记录" << endl;
		system("pause");
		system("cls");
		return;
	}
	cout << "审核中或预约成功的记录可以取消，请输入取消的记录" << endl;

	vector<int>v;
	int index = 1;
	for (int i = 0; i < of.oSize; i++)
	{
		if (atoi(of.oOrderData[i]["学号"].c_str()) == this->sNum)
		{
			if (of.oOrderData[i]["预约信息"] == "1" || of.oOrderData[i]["预约信息"] == "2")
			{
				v.push_back(i);
				cout << index++ << "、 ";
				cout << "预约日期： 周" << of.oOrderData[i]["日期"];
				cout << " 时段：" << (of.oOrderData[i]["时间段"] == "1" ? "上午" : "下午");
				cout << " 机房：" << of.oOrderData[i]["机房号"];
				string status = " 状态： ";  // 0 取消的预约   1 审核中   2 已预约  -1 预约失败
				if (of.oOrderData[i]["status"] == "1")
				{
					status += "审核中";
				}
				else if (of.oOrderData[i]["status"] == "2")
				{
					status += "预约成功";
				}
				cout << status << endl;

			}
		}
	}

	cout << "请输入取消的记录,0代表返回" << endl;
	int select = 0;
	while (true)
	{
		cin >> select;
		if (select >= 0 && select <= v.size())
		{
			if (select == 0)
			{
				break;
			}
			else
			{
				//	cout << "记录所在位置： " << v[select - 1] << endl;
				of.oOrderData[v[select - 1]]["预约信息"] = "0";
				of.oUpdateOrder();
				cout << "已取消预约" << endl;
				break;
			}

		}
		cout << "输入有误，请重新输入" << endl;
	}

	system("pause");
	system("cls");
}

